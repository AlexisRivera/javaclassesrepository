package org.schimpf.awt.image;

/**
 * Eventos del panel animado de imagen
 * 
 * @author <FONT style='color:#55A; font-size:12px; font-weight:bold;'>Hermann D. Schimpf</FONT>
 * @author <B>SCHIMPF</B> - <FONT style="font-style:italic;">Sistemas de Informaci&oacute;n y Gesti&oacute;n</FONT>
 * @author <B>Schimpf.NET</B>
 * @version May 27, 2012 9:44:49 PM
 */
public interface AnimatedImagePanelListener {
	/**
	 * Se ejecuta cuando se finaliza el posicionado de la imagen
	 * 
	 * @author <FONT style='color:#55A; font-size:12px; font-weight:bold;'>Hermann D. Schimpf</FONT>
	 * @author <B>SCHIMPF</B> - <FONT style="font-style:italic;">Sistemas de Informaci&oacute;n y Gesti&oacute;n</FONT>
	 * @author <B>Schimpf.NET</B>
	 * @version May 27, 2012 9:46:39 PM
	 * @param x Posicion horizontal de la imagen
	 * @param y Posicion vertical de la imagen
	 */
	public void positionationFinished(float x, float y);

	/**
	 * Se ejecuta cuando se finaliza la rotacion de la imagen
	 * 
	 * @author <FONT style='color:#55A; font-size:12px; font-weight:bold;'>Hermann D. Schimpf</FONT>
	 * @author <B>SCHIMPF</B> - <FONT style="font-style:italic;">Sistemas de Informaci&oacute;n y Gesti&oacute;n</FONT>
	 * @author <B>Schimpf.NET</B>
	 * @version May 27, 2012 9:45:14 PM
	 * @param degree Angulo de la imagen
	 */
	public void rotationFinished(float degree);
}